		// 자바의 데이터 타입에는 크게 기본타입(원시 타입 : primitive type)과 참조타입(reference type)으로 분류된다.
		
		// 기본타입이란 정수, 실수, 문자, 논리 리터럴을 저장하는 타입을 말한다.
