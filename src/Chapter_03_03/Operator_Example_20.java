package Chapter_03_03;

public class Operator_Example_20 {

	public static void main(String[] args) {

		// 비트 논리 연산자
		
		System.out.println("45 & 25 = " + (45 % 25));
		System.out.println("45 | 25 = " + (45 | 25));
		System.out.println("45 ^ 25 = " + (45 ^ 25));
		System.out.println(" ~45 = " + (~45));
	}

}
