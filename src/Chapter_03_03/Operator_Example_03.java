package Chapter_03_03;

public class Operator_Example_03 {

	public static void main(String[] args) {
		// 논리 부정 연산자
		
		boolean play = true;
		System.out.println("play = " + play);
		
		play = !play;
		System.out.println("!play = " + play);
		
		play = !play;
		System.out.println("play = " + play);
	} 

}
